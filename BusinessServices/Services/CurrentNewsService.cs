        }

        public ResponseModel AddUpdateCurrentNews(vmCurrentNews obj)
        {
            try
            {
                ResponseModel result = new ResponseModel();
                result = _unitOfWork.CurrentNewsRepository.AddUpdateCurrentNews(obj);

                response.status = result.status;
                response.message = result.message;

            }
            catch (Exception ex)
            {
                response.message = ex.Message;
                response.status = Status.Error;
            }
            return response;
        }
        public ResponseModel GetAllCurrentNews()
        {
            try
            {
                ResponseModel result = new ResponseModel();
                result = _unitOfWork.CurrentNewsRepository.GetAllCurrentNews();

                response.data = result.data;
                response.status = result.status;
                response.message = result.message;

            }
            catch (Exception ex)
            {
                response.message = ex.Message;
                response.status = Status.Error;
            }
            return response;
        }

        public ResponseModel deleteCurrentNews(int? Id)
        {
            try
            {
                ResponseModel result = new ResponseModel();
                result = _unitOfWork.CurrentNewsRepository.deleteCurrentNews(Id);

                response.data = result.data;
                response.status = result.status;
                response.message = result.message;

            }
            catch (Exception ex)
            {
                response.message = ex.Message;
                response.status = Status.Error;
            }
            return response;
        }


        public ResponseModel fileUploadInCurrentNews(vmfileInfo obj)
        {
            try
            {
                var resultValue = _unitOfWork.CurrentNewsRepository.fileUploadInCurrentNews(obj);
                if (resultValue != null)
                {
                    response.status = Status.Success;
                    response.data = resultValue;
                    response.message = "file saved";
                }
            }
            catch (Exception ex)
            {
                response.data = ex.Message;
                response.status = Status.Error;
                response.message = "Execption Occured.";
            }
            return response;
        }


        public ResponseModel GetCurrentNewsDetailByCurrentNewsId(int? Id)
        {
            try
            {
                ResponseModel result = new ResponseModel();
                result = _unitOfWork.CurrentNewsRepository.GetCurrentNewsDetailByCurrentNewsId(Id);

                response.data = result.data;
                response.status = result.status;
                response.message = result.message;

            }
            catch (Exception ex)
            {
                response.message = ex.Message;
                response.status = Status.Error;
            }
            return response;
        }


        public ResponseModel videoUploadInCurrentNews(vmfileInfo obj)
        {
            try
            {
                var resultValue = _unitOfWork.CurrentNewsRepository.videoUploadInCurrentNews(obj);
                if (resultValue != null)
                {
                    response.status = Status.Success;
                    response.data = resultValue;
                    response.message = "file saved";
                }
            }
            catch (Exception ex)
            {
                response.data = ex.Message;
                response.status = Status.Error;
                response.message = "Execption Occured.";
            }
            return response;
        }

        public ResponseModel deleteVideoInCurrentNews(string oldVideoName)
        {
            try
            {
                _unitOfWork.CurrentNewsRepository.deleteVideoInCurrentNews(oldVideoName);
                response.status = Status.Success;
                response.message = "file Delete Successfully";

            }
            catch (Exception ex)
            {
                response.data = ex.Message;
                response.status = Status.Error;
                response.message = "Execption Occured.";
            }
            return response;
        }
    }
